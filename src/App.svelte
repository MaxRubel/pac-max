  function startCounter() {
    intervalId = setInterval(() => {
      counter++;
      moveShape();
    }, INTERVAL_TIME);
  }

  function moveShape() {
    switch (direction) {
      case "right":
        posX++;
        if (posX >= CONTAINER_WIDTH) {
          posX = CONTAINER_WIDTH;
          direction = posY === 0 ? "up" : "down";
        }
        break;
      case "up":
        posY++;
        if (posY >= CONTAINER_HEIGHT) {
          posY = CONTAINER_HEIGHT;
          direction = posX === CONTAINER_WIDTH ? "left" : "right";
        }
        break;
      case "left":
        posX--;
        if (posX <= 0) {
          posX = 0;
          direction = posY === CONTAINER_HEIGHT ? "down" : "up";
        }
        break;
      case "down":
        posY--;
        if (posY <= 0) {
          posY = 0;
          direction = posX === 0 ? "right" : "left";
        }
        break;
    }

    function handleKeyDown(e: KeyboardEvent) {
      switch (e.key) {
        case "ArrowLeft":
          direction = "left";
          break;
        case "ArrowRight":
          direction = "right";
          break;
        case "ArrowDown":
          direction = "down";
          break;
        case "ArrowUp":
          direction = "up";
          break;
      }
    }

    document.addEventListener("keydown", handleKeyDown);

    // onMount(() => {
